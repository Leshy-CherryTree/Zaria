package eu.cherrytree.gdx.particles.editor;


/**
 * 
 * Branched from Particle Editor of libGDX in gdx-tools.
 */
public class Point
{
	//--------------------------------------------------------------------------
	
	public float x;
	public float y;
	
	//--------------------------------------------------------------------------

	public Point()
	{
		// Intentionally empty.
	}
	
	//--------------------------------------------------------------------------

	public Point(float x, float y)
	{
		this.x = x;
		this.y = y;
	}
	
	//--------------------------------------------------------------------------
}
